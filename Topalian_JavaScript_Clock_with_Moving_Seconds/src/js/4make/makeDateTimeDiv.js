function makeDateTimeDiv()
{
    let dateTimeDiv = ce('div');
    dateTimeDiv.id = 'dateTimeDiv';
    dateTimeDiv.title = 'dateTimeDiv';
    ge('mainDiv').append(dateTimeDiv);
}

