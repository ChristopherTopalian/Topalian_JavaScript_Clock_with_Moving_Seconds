function getMonthName()
{
    let dateTime = new Date();

    let month = dateTime.getMonth();

    if (month == 0)
    {
        return "January";
    }
    if (month == 1)
    {
        return "February";
    }
    if (month == 2)
    {
        return "March";
    }
    if (month == 3)
    {
        return "April";
    }
    if (month == 4)
    {
        return "May";
    }
    if (month == 5)
    {
        return "June";
    }
    if (month == 6)
    {
        return "July";
    }
    if (month == 7)
    {
        return "August";
    }
    if (month == 8)
    {
        return "September";
    }
    if (month == 9)
    {
        return "October";
    }
    if (month == 10)
    {
        return "November";
    }
    if (month == 11)
    {
        return "December";
    }
}

